/// Get word completions for the given prefix.
pub fn complete_words(prefix: &str) -> Vec<&'static str> {
    if prefix.is_empty() {
        return Vec::new();
    }

    let prefix_lower = prefix.to_lowercase();
    COMMON_WORDS
        .iter()
        .filter(|w| w.starts_with(&prefix_lower))
        .copied()
        .take(10)
        .collect()
}
